type Messages = typeof dictionary.ru;

interface I18nContextValue {
  lang: Lang;
  setLang: (lang: Lang) => void;
  t: (key: keyof Messages) => string;
  pick: (value: LocalizedText | string) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => (localStorage.getItem("bookshop-lang") as Lang) || "ru");

  const value = useMemo<I18nContextValue>(() => ({
    lang,
    setLang: (next) => {
      localStorage.setItem("bookshop-lang", next);
      setLangState(next);
    },
    t: (key) => dictionary[lang][key] || key,
    pick: (input) => {
      if (typeof input === "string") return input;
      if (!input) return "";
      return input[lang] || input.ru || "";
    },
  }), [lang]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider");
  }
  return context;
}
